fun fibSequenceDigit(n: Int): Int {
    var length = 0
    var number = 0
    while (length < n){
        number++
        length += digitNumber(fib(number))
    }
    var fibNum = fib(number)
    while (length != n) {
        fibNum /= 10
        length -= 1
    }
    return fibNum % 10
}

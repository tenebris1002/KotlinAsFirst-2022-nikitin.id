 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    var chN = n
    var ditNum = digitNumber(n) - 1
    var result = 0
    for (i in 1..digitNumber(n)){
        result += (chN % 10) * (10.0.pow(ditNum)).toInt()
        ditNum -= 1
        chN /= 10
    }
    return result
}

